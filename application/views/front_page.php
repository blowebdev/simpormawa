                    Fitur utama dari Sistem Informasi Pengajuan Proposal Kegiatan meliputi: <br>

                    1. Pengisian Proposal: Sistem ini menyediakan formulir pengajuan yang komprehensif, memungkinkan pengguna untuk mengisi informasi yang diperlukan, seperti judul kegiatan, deskripsi, tujuan, anggaran, jadwal, dan lain-lain.<br>

