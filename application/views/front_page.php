                    4. Notifikasi dan Pemberitahuan: Sistem ini akan mengirimkan notifikasi kepada pengguna mengenai status pengajuan proposal mereka. Pengguna akan menerima pemberitahuan jika ada perubahan status atau permintaan tambahan informasi.<br>

                    5. Komentar dan Kolaborasi: Sistem ini juga menyediakan fitur kolaborasi yang memungkinkan pengguna untuk berinteraksi dan memberikan komentar terhadap proposal yang diajukan. Hal ini memungkinkan diskusi dan pembaruan yang diperlukan antara pemohon dan tim verifikasi.<br>

                    6. Penyimpanan dan Akses Arsip: Sistem ini akan menyimpan proposal yang diajukan sebagai arsip digital. Pengguna yang berwenang dapat mengakses arsip ini untuk melihat proposal yang pernah diajukan, memudahkan proses pemantauan dan evaluasi.<br>

                    Dengan adanya Sistem Informasi Pengajuan Proposal Kegiatan, proses pengajuan proposal menjadi lebih terstruktur, transparan, dan efisien. Ini memungkinkan pemohon dan pihak yang berwenang untuk berinteraksi secara efektif dan memastikan bahwa proposal kegiatan yang diajukan sesuai dengan persyaratan dan kebijakan yang berlaku.<br>
